<strong>Synopses and review:</strong><br>
The third in a series of classic, collectible cookbooks from Tartine Bakery & Cafe, one of the great bakeries, Tartine Book No. 3 is a revolutionary, and altogether timely, exploration of baking with whole grains. The narrative of Chad Robertson's search for ancient flavors in heirloom grains is interwoven with 85 recipes for whole-grain versions of Tartine favorites. Robertson shares his groundbreaking new methods of bread baking including new techniques for whole-grain loaves, as well as porridge breads and loaves made with sprouted grains. This book also revisits the iconic Tartine Bakery pastry recipes, reformulating them to include whole grains, nut milks, and alternative sweeteners. More than 100 photographs of the journey, the bread, the pastry and the people make this is a must-have reference for the modern baker.<br>

