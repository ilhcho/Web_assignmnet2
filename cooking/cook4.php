<strong>Synopses and review:</strong><br>The author goes deep behind the scenes to explore the kitchens, personal lives, and mindsets of Portland's celebrated cooks to chronicle, with humor and panache, a people's army of maverick chefs, artisans, obsessives, farmers, food carters, and plucky pioneers who have created a risk-taking, no rules food town unlike any other: one that is exporting its culinary ethos, innovations, and sensibilities to America's gastronomic power zones in New York, LA, Chicago, and countless other cities that are coming under its spell: a spell and culinary imagination that, according to Bon Appetit Restaurant and Drinks Editor, Andrew Knowlton, emanates from "a city thriving with creativity, passion, and an anything goes attitude for all things edible."<br>

<br>Among Portland chefs whose work, culinary output, and cooking pearls are profiled in this book are those whose rise-to-stardom stories, picture-perfect dishes, and iconoclastic innovations now slather the pages of Bon Appetit, Saveur, the New York Times, the Wall Street Journal, Food and Wine, GQ and The Huffington Post; as well as the Food Channel. The authors introduce you to Pok Pok's Andy Ricker, a pioneering, chicken wingmeister who gambled all on a Thai chicken shack in his front yard and now rules the roost in New York; and Le Pigeon's Gabriel Rucker, the freewheeling flavor genius (and James Beard Rising Star 2011) who power drills lamb heads in his basement like a hit man in Goodfellas. You'll also encounter the artisanal know-how at Olympic Provisions, whose chefs have spawned their own Slow Salumi movement; and chocolatier David Briggs, whose nationally celebrated Raleigh Bar — he makes them in the storeroom of a cult sandwich shop — has re-imagineered the iconic Snickers bar, catapulting this salted caramel-meets-pecan and chocolate nougat confection into a new galaxy of pleasure, prompting Bon Appetit's Andrew Knowlton to fawn, "the best chocolate candy I've had."<br>

<br>In The Mighty Gastropolis, you will learn, ingredient by ingredient, experiment by experiment, dish by dish, how Portland's culinary cognoscenti have re-imagined and reconfigured restaurant culture for modern times and established a new paradigm for how to succeed in the fiercely competitive, no-chops-barred worlds of both hi- and lo-fi dining. The result, as Thomas Lauderdale, founder of Pink Martini, explains,"is a hilarious, heart-warming, punk-rock portrait of a daringly creative Mecca showing the rest of America a better way to eat — and live."<br>

<br>This is a landmark contribution to the literature of food. And, perhaps best of all, the book's recipes are roadmaps to rarified states of gastro-nirvana.

