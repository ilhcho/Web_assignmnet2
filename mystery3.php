From the award-winning author of One False Move comes Harlan Coben's most mesmerizing and emotionally charged novel to date.<br>

<br>Myron Bolitar, sports agent and reluctant sleuth, is happily basking in the sun of the Caribbean, clearing his head with a woman he hardly knows, when Win, his loyal but morally questionable sidekick, arrives to tell him that Esperanza, Myron's partner at MB SportsReps and best friend, has been arrested for the murder of Clu Haid, a fallen baseball star and Bolitar client.<br>

