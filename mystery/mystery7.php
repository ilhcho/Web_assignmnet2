<br>An activist attorney is killed in a cute little L.A. trolley called Angels Flight, far from Harry Bosch's Hollywood turf. But the case is so explosive--and the dead man's enemies inside the L.A.P.D. are so numerous--that it falls to Harry to solve it. Now the streets are superheating. Harry's year-old Vegas marriage is unraveling. And the hunt for a killer is leading Harry to another high-profile L.A. murder case, one where every cop had a motive. The question is, did any have the guts?<br>

<br>"Connelly's novel follows series hero Harry Bosch's investigation into the murder of an African-American defense attorney who made a career of courtroom victories at the expense of the Los Angeles Police Department. This installment in the series is especially dark, and narrator Peter Giles's reads in a voice that echoes with the dry croaking of a lifelong smoker â€” something that establishes a noirlike mood from the get-go. The narrator ably matches Bosch's downbeat mood, shifting from anger at having to deal with racism, not just in his city but within the ranks of the LAPD, to weariness, sadness, and frustration at his inability to stop the disintegration of his marriage. Giles sands some of the roughness from his voice and pitches it slightly higher for the book's female characters, like the detective's soon-to-be-separated wife and his partner, Kiz Rider. But there's still an edge rough enough to remind us we're not listening to an Agatha Christie cozy. A Grand Central paperback. (June)" Publishers Weekly Copyright PWxyz, LLC. All rights reserved.

