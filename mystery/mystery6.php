<br>&nbsp;&nbsp;"Meyer's superbly crafted South African thriller follows the interconnected lives of three characters: Christine van Rooyen, a beautiful but luckless Capetown prostitute who gets on &nbsp;&nbsp;the bad side of the brother of a Colombian drug lord; Thobela Mpayipheli, a former mercenary whose retirement ends with the murder of his eight-year-old son; and veteran police &nbsp;&nbsp;inspector Benny Griessel, an alcoholic burnout struggling to regain sobriety and the respect of his family and co-workers. Narrator Simon Vance turns in a successful performance &nbsp;&nbsp;in this audio edition. When Benny's mood swings from despair to hope, Vance's hoarse croak reflects the changes. Similarly, the narrator's rendition of Thobela changes from an &nbsp;&nbsp;initial paternal cheeriness when speaking with his son to despair at the boy's death and stoic flatness as he sets out on his quest for vengeance. Emotions run high throughout, &nbsp;&nbsp;and Vance successfully conveys every moment of despair and elation. But it's near the novel's end — when the plot strands connect in a suspenseful and violent confrontation — &nbsp;&nbsp;that Vance pulls out all the stops, adding a notable finishing touch to this compelling presentation. (July)" Publishers Weekly Copyright PWxyz, LLC. All rights reserved.


