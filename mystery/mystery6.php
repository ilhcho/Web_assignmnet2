&nbsp;&nbsp;<strong>Synopses and review:</strong><br>
&nbsp;&nbsp;A young woman makes a terrible confession to a priest. An honorable man takes his own revenge for an unspeakable tragedy. An aging inspector tries to get himself sober while &nbsp;&nbsp;taking on the most difficult case of his career. From this beginning, Deon Meyer weaves a story of astonishing complexity and suspense, as Inspector Benny Griessel faces off &nbsp;&nbsp;against a dangerous vigilante who has everything on his side, including public sympathy.<br>

